kTe = 10*1.602*10^(-19);            %[eV]-> [J]換算
mi_Ar = 6.6*10^(-23)*10^(-3);       %[kg]
mi_H = 1.0/((6.0*10^(23))*10^(3));  %[kg]
S = 4.0 * 10^(-6);                  %[m^(-2)]

I = 0.61 *e*n_e*sqrt(kTe/mi_H)*S

